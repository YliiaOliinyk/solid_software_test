part of 'color_bloc.dart';

///Color State class which holds the color values
class ColorState {
  ///Red color value
  final int red;

  ///Green color value
  final int green;

  ///Blue color value
  final int blue;

  ///Constructor
  const ColorState({
    required this.red,
    required this.green,
    required this.blue,
  });
}
